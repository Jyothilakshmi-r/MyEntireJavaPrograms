package Assignments;

public class MathematicalOperations {
	
	static void add()
	{
		int a=50;
		int b=5;
		int result=a+b;
		System.out.println(result);
	}
	static void sub()
	{
		int a=50;
		int b=10;
		int result=a-b;
		System.out.println(result);
	}
	static void mul()
	{
		int a=5;
		int b=50;
		int result=a*b;
		System.out.println(result);
	}
	static void div()
	{
		int a=50;
		int b=50;
		int result=a/b;
		System.out.println(result);
	}
	static void mod()
	{
		int a=500;
		int b=50;
		int result=a%b;
		System.out.println(result);
	}
	public static void main(String[] args) 
	{
		add();
		sub();
		mul();
		div();
		mod();

	}
}
