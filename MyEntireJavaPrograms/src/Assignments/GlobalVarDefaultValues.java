package Assignments;

public class GlobalVarDefaultValues
{
	static byte b;
	static int a;
	static short s;
	static long l;
	static float f;
	static double d;
	static boolean bl;
	static char c;
	static String st;
	
	public static void main(String[] args) 
	{
		System.out.println("byte b = "+ b);
		System.out.println("int a = "+ a);
		System.out.println("short s = "+ s);
		System.out.println("long l = "+ l);
		System.out.println("float f = "+ f);
		System.out.println("double d = "+ d);
		System.out.println("boolean bl = "+ bl);
		System.out.println("char c = "+ c); //not defined in java, so no output
		System.out.println("string st = "+ st);
		
	}

}
